# Arithmetic Operator

x = int(input("enter X value..."))
y = 2

# Addition

def addition():
    z = x+y
    print("Addition is...   ",z)
    print("\n")

addition()

# Substraction

def substraction():
    z = x-y
    print("Substraction is...   ",z)
    print("\n")

substraction()

# multiplication

def multiplication():
    z = x*y
    print("Multiplication is...   ",z)
    print("\n")

multiplication()

# Division

def division():
    z = x/y
    print("Division is...   ",z)
    print("\n")

division()

# Division (floor)

def division_f():
    z = x//y
    print("division floor is...   ",z)
    print("\n")

division_f()

# modulus

def module():
    z = x%y
    print("Module is...   ",z)
    print("\n")

module()

# Power

def power():
    z = x**y
    print("Power is...   ",z)
    print("\n")

power()
    
