# Bitwise Operator

a = 20
b = 10

# Bitwise AND &

def And():
    c = a&b
    print("Value of Bitwise AND is...   ",c)
    print("\n")

And()

# Bitwise OR |

def Or():
    c = a|b
    print("Value of Bitwise OR is...   ",c)
    print("\n")

Or()

# Bitwise NOT ~

def Not():
    c = ~b
    print("Value of Bitwise NOT is...   ",c)
    print("\n")

Not()

# Bitwise XOR ^

def Xor():
    c = a^b
    print("Value of Bitwise XOR is...   ",c)
    print("\n")

Xor()

# Bitwise Right >>

def Right():
    c = a>>b
    print("Value of Bitwise Right is...   ",c)
    print("\n")

Right()

# Bitwise Left <<

def Left():
    c = a<<b
    print("Value of Bitwise Left is...   ",c)
    print("\n")

Left()
