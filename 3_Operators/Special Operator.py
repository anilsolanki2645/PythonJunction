# Special Operator

# [1] Identity Operator

# is

x = 10
y = x

if x is y:
    print("Both Variable are same....")
else:
    print("Not Same...")

print("\n")

#is not

if x is not y:
    print("Both variable are not same ....")
else:
    print("Both are same...")

print("\n")

# [2] Membership Operator

#in

a = "I am Student Of PGI"

if 'am' in a:
    print("String are Match...")
else:
    print("String are not match....")
print("\n")


#not in

if 'ok' not in a:
    print("String are match...")
else:
    print("String are Not Match...")






