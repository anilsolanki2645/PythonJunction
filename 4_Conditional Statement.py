# Conditional Statement or Decision maker

# [1] if

num = 5

print("*** IF Statement ***")
if num<10:
    print("True")
print("\n")

# [2] if-else

print("*** IF-ELSE Statement ***")
if(num<2):
    print("True")
else:
    print("False")
print("\n")

# [3] elif

print("*** ELIF Statement ***")
if(num<2):
    print("True")
elif(num>3):
    print("True")
else:
    print("False")
print("\n")

# [4] Nested If

print("*** NESTED-IF Statement ***")
if(num>0):
    print("Positive Number....")
    if(num<10):
        print("True, Num is Less than 10...")




    
